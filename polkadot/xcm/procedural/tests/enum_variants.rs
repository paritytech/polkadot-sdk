//! Test the struct generated by the `NumVariants` derive macro.

use frame_support::traits::Get;
use xcm_procedural::NumVariants;

#[allow(dead_code)]
#[derive(NumVariants)]
enum SomeEnum {
	Variant1,
	Variant2,
	Variant3,
}

#[test]
fn num_variants_works() {
	assert_eq!(SomeEnumNumVariants::get(), 3);
}
