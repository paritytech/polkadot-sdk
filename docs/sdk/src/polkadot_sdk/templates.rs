    //! # Templates
    //!
    //! This document enumerates a non-exhaustive list of templates that one can use to get started with
    //! polkadot-sdk.
    //!
    //! > Know more tools/templates that are not listed here? please contribute them by opening a PR.
    //!
    //! These templates are always kept up to date, based from the templates in the mono-repo. With these, you can
    //! easily get hacking on your Substrate nodes:
    //!
    //! - <https://github.com/paritytech/polkadot-sdk-solochain-template>: A white-labeled Substrate-based blockchain (aka. solochain) that
    //!   contains moderate features, such as a basic consensus engine and some FRAME pallets.
    //! //! - <https://github.com/paritytech/polkadot-sdk-minimal-template>: A minimal template that contains the
    //!   least amount of features to be a functioning blockchain. Suitable for learning, development
    //!   and testing. This template is not meant to be used in production.
    //! - <https://github.com/paritytech/polkadot-sdk-parachain-template>: A template for developers looking to create a node designed
    //! to interoperate with Polkadot. 
    //!
    //! ## External Templates
    //!
    //! Noteworthy templates outside of this repository.
    //!
    //! - [`extended-parachain-template`](https://github.com/paritytech/extended-parachain-template): A
    //!   parachain template that contains more built-in functionality such as assets and NFTs.
    //! - [`frontier-parachain-template`](https://github.com/paritytech/frontier-parachain-template): A
    //!   parachain template for launching EVM-compatible parachains.
    //!
    //! ## OpenZeppelin
    //!
    //! In June 2023, OpenZeppelin was awarded a grant from the [Polkadot
    //! treasury](https://polkadot.polkassembly.io/treasury/406) for building a number of Polkadot-sdk
    //! based templates. These templates are expected to be a great starting point for developers.
    //!
    //! - <https://github.com/OpenZeppelin/polkadot-runtime-template/>
    //!
    //! ## POP-CLI
    //!
    //! POP CLI is a tool to help anyone launch and configure Polkadot-ready parachains with smart contracts out of the box.
    //! It makes runtime configuration and paracahin building a breeze.
    //!
    //! - <https://pop.r0gue.io/cli/>
